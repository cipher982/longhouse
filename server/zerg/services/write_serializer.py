def _env_float(name: str, default: float) -> float:
    raw = os.getenv(name)
    if raw is None or raw.strip() == "":
        return default
    try:
        return float(raw)
    except ValueError:
        return default


def _env_bool(name: str, default: bool) -> bool:
    raw = os.getenv(name)
    if raw is None or raw.strip() == "":
        return default
    return raw.strip().lower() in _TRUTHY_ENV


def _utc_now_iso() -> str:
    return datetime.now(timezone.utc).isoformat().replace("+00:00", "Z")


def _sqlite_connection_from_session(db: Session) -> sqlite3.Connection | None:
    """Best-effort access to the DB-API sqlite connection owned by this Session."""

    try:
        sqlalchemy_connection = db.connection()
    except Exception:
        return None

    candidate: object = getattr(sqlalchemy_connection, "connection", None)
    seen: set[int] = set()
    while candidate is not None and id(candidate) not in seen:
        if isinstance(candidate, sqlite3.Connection):
            return candidate
        seen.add(id(candidate))
        next_candidate = getattr(candidate, "driver_connection", None)
        if next_candidate is None:
            next_candidate = getattr(candidate, "connection", None)
        candidate = next_candidate
    return None


def _looks_like_sqlite_interrupt(exc: BaseException) -> bool:
    if isinstance(exc, sqlite3.OperationalError) and "interrupted" in str(exc).lower():
        return True
    orig = getattr(exc, "orig", None)
    if isinstance(orig, BaseException) and _looks_like_sqlite_interrupt(orig):
        return True
    return False


@dataclass
class _LabelHistogram:
    """Rolling per-label timing window. Bounded deque -> exact percentiles."""

    queue_wait_ms: Deque[float] = field(default_factory=lambda: deque(maxlen=_HISTOGRAM_WINDOW))
    exec_ms: Deque[float] = field(default_factory=lambda: deque(maxlen=_HISTOGRAM_WINDOW))


@dataclass
class WriteStats:
    """Cumulative write serializer statistics."""

    total_writes: int = 0
    total_queue_wait_ms: float = 0
    total_exec_ms: float = 0
    max_queue_wait_ms: float = 0
    max_exec_ms: float = 0
    errors: int = 0
    _label_counts: dict[str, int] = field(default_factory=dict)
    _label_histograms: dict[str, _LabelHistogram] = field(default_factory=dict)

    def record_sample(self, label: str, queue_wait_ms: float, exec_ms: float) -> None:
        key = label or "unlabeled"
        hist = self._label_histograms.get(key)
        if hist is None:
            hist = _LabelHistogram()
            self._label_histograms[key] = hist
        hist.queue_wait_ms.append(queue_wait_ms)
        hist.exec_ms.append(exec_ms)


def _percentiles(samples: Deque[float]) -> dict[str, float]:
    """Return p50/p95/p99 of an unsorted deque. Empty -> zeros."""
    if not samples:
        return {"p50": 0.0, "p95": 0.0, "p99": 0.0, "n": 0}
    sorted_samples = sorted(samples)
    n = len(sorted_samples)

    def _pct(p: float) -> float:
        idx = min(n - 1, max(0, int(round((p / 100.0) * (n - 1)))))
        return round(sorted_samples[idx], 1)

    return {"p50": _pct(50), "p95": _pct(95), "p99": _pct(99), "n": n}


