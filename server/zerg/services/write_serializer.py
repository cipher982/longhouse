def request_session_released_by_serializer(ws: object) -> bool:
    """Return true when ``execute_after_closing_request_session`` closed fallback DB."""

    return bool(getattr(ws, "is_configured", False)) and os.getenv("TESTING", "").strip().lower() not in _TRUTHY_ENV


@contextmanager
def post_write_db_session(ws: object, fallback_db: Session) -> Iterator[Session]:
    """Yield a usable DB session after a request-session-releasing write."""

    if request_session_released_by_serializer(ws):
        from zerg.database import get_session_factory

        SessionLocal = get_session_factory()
        with SessionLocal() as db:
            yield db
        return
    yield fallback_db


def post_write_fallback_db(ws: object, fallback_db: Session) -> Session | None:
    """Return a safe fallback DB for deferred post-write helpers."""

    if request_session_released_by_serializer(ws):
        return None
    return fallback_db


async def execute_post_write(
    ws: object,
    fn: Callable[[Session], T],
    fallback_db: Session | None,
    *,
    label: str = "",
    priority: int | None = None,
    auto_commit: bool = True,
    timeout_seconds: float | None = None,
    queue_timeout_seconds: float | None = None,
) -> T:
    """Execute a follow-up write after a request-session-releasing write."""

    execute_or_direct = getattr(ws, "execute_or_direct")
    kwargs: dict[str, object] = {
        "label": label,
        "auto_commit": auto_commit,
    }
    if priority is not None:
        kwargs["priority"] = priority
    if timeout_seconds is not None:
        kwargs["timeout_seconds"] = timeout_seconds
    if queue_timeout_seconds is not None:
        kwargs["queue_timeout_seconds"] = queue_timeout_seconds
    if fallback_db is None:
        return await execute_or_direct(fn, None, **kwargs)
    with post_write_db_session(ws, fallback_db) as db:
        return await execute_or_direct(fn, db, **kwargs)


class WriteSerializer:
    """Serialize all SQLite writes through a single lock.

    Ensures only one write transaction runs at a time, eliminating
    'database is locked' errors from concurrent background writers.
    """

    def __init__(self) -> None:
        self._wait_cond = asyncio.Condition()
        self._session_factory: sessionmaker | None = None
        self._session_factory_resolver: Callable[[], sessionmaker] | None = None
        self._stats = WriteStats()
        self._configured = False
        self._queue: list[_QueuedWrite] = []
        self._next_seq = 0
        self._writer_active = False
        self._active_label: str | None = None
        self._active_priority: int | None = None
        self._active_job_id: int | None = None
        self._active_worker_thread_id: int | None = None
        self._active_sqlite_connection: sqlite3.Connection | None = None
        self._active_interrupt_requested = False
        self._active_interrupt_after_seconds: float | None = None
        self._active_stage: str | None = None
        self._active_started_at: float | None = None
        self._active_stack_dump_count = 0
        self._active_interrupt_count = 0
        self._active_wedged_writer_count = 0
        self._fairness_promotions = 0
        self._last_active_interrupt_at_iso: str | None = None
        self._last_active_interrupt_age_ms: float | None = None
        self._last_active_interrupt_label: str | None = None
        self._last_active_wedged_writer_at_iso: str | None = None
        self._last_active_wedged_writer_age_ms: float | None = None
        self._last_active_wedged_writer_label: str | None = None
        self._last_active_wedged_writer_reason: str | None = None
        self._last_active_stack_dump_at: float | None = None
        self._last_active_stack_dump_iso: str | None = None
        self._last_active_stack_dump_age_ms: float | None = None
        self._last_active_stack_dump_reason: str | None = None
        self._last_active_stack_dump_text: str | None = None
        self._background_tasks: set[asyncio.Task[None]] = set()
        self._change_event = asyncio.Event()
        self._change_seq = 0

    def configure(self, session_factory: sessionmaker) -> None:
        """Set the write session factory. Call once at startup."""
        self._session_factory = session_factory
        self._session_factory_resolver = None
        self._configured = True
        _last_write_timing.set(None)
        logger.info("WriteSerializer configured")

